def Somma():
    n_1=int(input("Inserisci il primo numero: "))
    n_2=int(input("inserisci il secondo numero: "))
    risultato = n_1 + n_2
    print(f"{n_1} + {n_2} = {risultato}")

def Sottrazione():
    n_1=int(input("Inserisci il primo numero: "))
    n_2=int(input("inserisci il secondo numero: "))
    risultato = n_1 - n_2
    print(f"{n_1} - {n_2} = {risultato}")
